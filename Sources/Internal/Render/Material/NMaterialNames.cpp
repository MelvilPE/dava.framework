bool NMaterialFlagName::IsRuntimeFlag(const FastName& flag)
{
    auto it = std::find_if(RUNTIME_ONLY_FLAGS.begin(), RUNTIME_ONLY_FLAGS.end(), [&flag](const FastName& toFind)
                           { return flag == toFind; });
    return it != RUNTIME_ONLY_FLAGS.end();
}

